package com.congthanh.project.constant.enums;

public enum PromotionType {
    DISCOUNT, //(Giảm giá): Khách hàng được giảm một tỷ lệ phần trăm hoặc một số tiền cố định trên giá bán của sản phẩm.
    SALE, //(Bán hàng giảm giá): Chương trình bán hàng giảm giá, thường áp dụng trong một khoảng thời gian giới hạn, với mức giảm giá cao hơn thông thường.
    VOUCHER, //Coupon (Phiếu giảm giá): Khách hàng có thể sử dụng mã voucher/coupon để được giảm giá khi thanh toán.
    BUY_ONE_GET_ONE,// (Mua 1 tặng 1): Khi mua một sản phẩm, khách hàng sẽ được tặng thêm một sản phẩm khác (cùng loại hoặc khác loại).
    FLASH_SALE, //(Giảm giá chớp nhoáng): Chương trình giảm giá đặc biệt trong một khoảng thời gian rất ngắn, thường là vài giờ.
    FREE_SHIPPING, //(Miễn phí vận chuyển): Khuyến mãi miễn phí vận chuyển khi đơn hàng đạt mức giá trị tối thiểu hoặc cho một số sản phẩm cụ thể.
    LOYALTY_PROGRAM, //(Chương trình khách hàng thân thiết): Khách hàng tích lũy điểm thưởng qua các lần mua hàng để đổi quà hoặc nhận ưu đãi trong tương lai.
    BUNDLE_DEAL, //(Ưu đãi khi mua theo gói): Giảm giá khi mua nhiều sản phẩm cùng lúc (ví dụ: mua 3 tặng 1 hoặc giảm giá khi mua combo).
    FIRST_PURCHASE_DISCOUNT, //(Giảm giá cho lần mua đầu tiên): Giảm giá đặc biệt cho khách hàng mới khi thực hiện lần mua hàng đầu tiên.
    REFERRAL_DISCOUNT, //(Giảm giá khi giới thiệu bạn bè): Khách hàng nhận được ưu đãi khi giới thiệu bạn bè sử dụng dịch vụ hoặc mua sản phẩm từ ứng dụng.
    SEASONAL_PROMOTION, //(Khuyến mãi theo mùa): Giảm giá hoặc ưu đãi đặc biệt theo các dịp lễ hội hoặc mùa trong năm, như Black Friday, Giáng sinh, Tết Nguyên Đán.
    CASHBACK
}
